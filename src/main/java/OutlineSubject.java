    private void continueButtonActionPerformed(java.awt.event.ActionEvent evt) {                                               

        if (img.getRoi() != null) {

            Roi roi = img.getRoi();

            Rectangle rec = roi.getBounds();

            int x = rec.x;
            int y = rec.y;
            int w = rec.width;
            int h = rec.height;

            img.setRoi(x, y, w, h);

            IJ.run(img, "Crop", "");

            AAPC_Var.hideOutlineSubject();

            WandFrame wf = new WandFrame();
            wf.setVisible(true);

            AAPC_Var.setWandFrame(wf);

        } else {
            IJ.error("You must first outline the subject before isolation");
        }

    }                                              

    private void setLanguage() {

        String s;

        switch (language) {

            case 0:

                s = "Outline the subject, then click continue.";

                toInstructionWidow(s, false);
                continueButton.setText("Continue...");
                break;

            case 1:

                s = "Skizzieren Sie das Thema und klicken Sie auf weiter.";
                toInstructionWidow(s, false);
                continueButton.setText("Weiter...");
                break;

            case 2:

                s = "DÃ©crivez l'objet, puis cliquez sur continuer.";
                toInstructionWidow(s, false);
                continueButton.setText("Continuer...");
                break;

            case 3:

                s = "Delineare il soggetto, quindi su Continua.";
                toInstructionWidow(s, false);
                continueButton.setText("Continuare...");
                break;

            case 4:

                s = "Kiswahili Inastrucions";
                toInstructionWidow(s, false);
                continueButton.setText("ugh...");
                break;

        }

    }

    private void toInstructionWidow(String s, Boolean go) {
        if (go) {
            String s1 = instructionTextArea.getText();
            String s2 = "\n*******************\n";
            instructionTextArea.setText(s1 + s2 + s);
        } else {
            instructionTextArea.setText(s);
        }
    }

